/** Icono lucide de una métrica: por el campo `icon` del pack; fallback CircleDot. */
export function getMetricIcon(id: string): LucideIcon {
  const name = DS_METRICS_BY_ID[id]?.icon;
  return (name && ICON_REGISTRY[name]) || CircleDot;
}

/** Resuelve un icono por nombre lucide (fallback CircleDot). */
export function iconByName(name?: string): LucideIcon {
  return (name && ICON_REGISTRY[name]) || CircleDot;
}
